package Day1;

public class Example3 {

	public static void main(String[] args) {
		String name = "Siddhi";
		int age = 22;
		//Before updating age
		System.out.println("Name:"+" "+name+" "+"Age:"+age);
		age = 23;
		//Printing the name and age together after updating age
		System.out.println("Name:"+" "+name+" "+"Age:"+age);
		name = "Shrutam";
		//after updating name
		System.out.println("Name:"+" "+name+" "+"Age:"+age);
	}

}
