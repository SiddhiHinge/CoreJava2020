package Day2.methods;

public class Eg1 {
	public static void main(String[] args) {
		add();
		substract();
	}
	public static void add() {
		int x = 10;
		int y = 20;
		int ans = x+y;
		System.out.println("Addition : "+ans);
	}
	
	public static void substract() {
		int x = 10;
		int y = 20;
		int ans = x-y;
		System.out.println("Substraction : "+ans);
	}
}
