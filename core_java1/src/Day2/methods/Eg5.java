package Day2.methods;

public class Eg5 {
	
	public static int add(int x,int y) {
		int ans = x+y;
		return ans;
	}
	
	public static int substract(int x,int y) {
		int ans = x-y;
		return ans;
	}
	
	public static int multiply(int x,int y) {
		int ans = x*y;
		return ans;
	}
	
	public static int divide(int x,int y) {
		int ans = x/y;
		return ans;
	}
}
